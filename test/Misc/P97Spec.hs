module Misc.P97Spec (spec) where

import Misc.P97
import Test.Hspec

spec :: Spec
spec = do
  describe "sudoku" $ do
    it "solves a Sudoku puzzle" $ do
      let board =
            [ [0, 0, 4, 8, 0, 0, 0, 1, 7],
              [6, 7, 0, 9, 0, 0, 0, 0, 0],
              [5, 0, 8, 0, 3, 0, 0, 2, 4],
              [3, 0, 0, 7, 4, 0, 1, 0, 0],
              [0, 6, 9, 0, 0, 0, 7, 8, 0],
              [0, 0, 1, 0, 6, 9, 0, 0, 5],
              [1, 0, 0, 0, 8, 0, 3, 0, 0],
              [0, 0, 0, 0, 0, 6, 0, 9, 1],
              [2, 4, 0, 0, 0, 1, 5, 0, 0]
            ]
      sudoku board
        `shouldBe` [ [9, 3, 4, 8, 2, 5, 6, 1, 7],
                     [6, 7, 2, 9, 1, 4, 8, 5, 3],
                     [5, 1, 8, 6, 3, 7, 9, 2, 4],
                     [3, 2, 5, 7, 4, 8, 1, 6, 9],
                     [4, 6, 9, 1, 5, 3, 7, 8, 2],
                     [7, 8, 1, 2, 6, 9, 4, 3, 5],
                     [1, 9, 7, 5, 8, 2, 3, 4, 6],
                     [8, 5, 3, 4, 7, 6, 2, 9, 1],
                     [2, 4, 6, 3, 9, 1, 5, 7, 8]
                   ]
